        $(".fa-spinner").hide();
        $("#sendMsg").click(function () {
            $(".fa-spinner").show();
            $("#sendMsg").html('<i class="fa fa-spinner fa-spin fa-2x"></i>');
            let name = $("#sendername").val();
            let email = $("#emailaddress").val();
            let message = $("#message").val();
            $.ajax({
                type: "POST",
                url: "process.php",
                data: { contact: "contact", name: name, email: email, message: message },
                success: function (data) {
                    if (data == "Message successfully sent. We will be in touch!") {
                        swal("Thanks!", data, "success");
                    } else {
                        swal("Error!", data, "error");
                    }
                    $("#sendMsg").html('Send Message <i class="fa fa-spinner fa-spin fa-2x"></i>');
                    $(".fa-spinner").hide();
                },
            });
        });

        $("#message").keyup(function(){
            let value = $(this).val();
            let count = value.length;
            let remaining = 500 - count;
            $(".wordCount").html(remaining + ' characters');

            if(count >= 500){
                $(this).val(value.substr(0,500));
                $(".wordCount").html('0 characters');
            }
        });
