      var placeholderIndex = 0;
      changePlaceholder();

      function changePlaceholder() {
        const placeholders = [
          "Search for a question...",
          "What is PennyTree",
          "How do I start using PennyTree?",
          "How do I get verified on PennyTree?",
          "How much can I start saving with?",
          "How much interest can I get with my savings plan?",
          "How many savings plans can I create on PennyTree?",
          "How long can I keep my savings with PennyTree?",
          "How can I add more money to my savings?",
          "What is Personal Vault?",
        ];
        $("#searchFAQs").attr("placeholder", placeholders[placeholderIndex]);
        placeholderIndex++;
        if (placeholderIndex > placeholders.length) {
          placeholderIndex = 0;
        }
        setTimeout(changePlaceholder, 3000);
      }

      $(document).ready(function () {
        (function ($) {
          $("#searchFAQs").keyup(function () {
            let filter = $(this).val();

            if (filter == "") {
              $(".searchable .card").show();
            } else {
              $(".collapse.show").removeClass("show");

              $(".searchable .card").hide();

              let regex = new RegExp(filter, "i");

              let filterResult = $(".searchable .card").filter(function () {
                return regex.test($(this).text());
              });

              if (filterResult.length != 0) {
                filterResult.show();
              }
            }
          });
        })($);
      });
